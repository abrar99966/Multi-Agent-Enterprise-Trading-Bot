"""PaperBroker unit tests: anti-lookahead, fill pricing, fees."""
from __future__ import annotations

from typing import Optional

import pytest

from app.core.clock import SimClock
from app.core.events import (
    NS_PER_SEC,
    Bar,
    Fill,
    Order,
    OrderStatus,
    OrderType,
    OrderUpdate,
    Side,
    Streams,
)
from app.paper.broker import PaperBroker
from tests.helpers import SyncTestBus

T0 = 1_700_000_000 * NS_PER_SEC
INTERVAL_S = 60
BAR_NS = INTERVAL_S * NS_PER_SEC

SLIPPAGE_BPS = 2.0
FEE_BPS = 1.0


@pytest.fixture()
def env() -> tuple[SyncTestBus, SimClock, PaperBroker]:
    clock = SimClock(start_ns=T0)
    bus = SyncTestBus(clock=clock)
    broker = PaperBroker(bus, clock, slippage_bps=SLIPPAGE_BPS, fee_bps=FEE_BPS)
    return bus, clock, broker


def make_bar(
    ts_open: int,
    *,
    symbol: str = "RELIANCE",
    o: float = 100.0,
    h: float = 101.0,
    lo: float = 99.0,
    c: float = 100.5,
) -> Bar:
    return Bar(
        symbol=symbol,
        ts_open=ts_open,
        interval_s=INTERVAL_S,
        open=o,
        high=h,
        low=lo,
        close=c,
        volume=10_000.0,
    )


def make_order(
    order_id: str,
    *,
    symbol: str = "RELIANCE",
    side: Side = Side.BUY,
    qty: float = 10.0,
    order_type: OrderType = OrderType.MARKET,
    limit_price: Optional[float] = None,
) -> Order:
    return Order(
        order_id=order_id,
        intent_id=f"int-{order_id}",
        strategy_id="strat-1",
        symbol=symbol,
        side=side,
        qty=qty,
        order_type=order_type,
        limit_price=limit_price,
    )


def publish_bar(bus: SyncTestBus, bar: Bar) -> None:
    bus.publish(Streams.MD_BARS, bar, ts_event=bar.ts_close)


def publish_order(bus: SyncTestBus, clock: SimClock, order: Order) -> None:
    bus.publish(Streams.EXEC_ORDERS, order, ts_event=clock.now_ns())


def fills(bus: SyncTestBus) -> list[Fill]:
    return [e.decode() for e in bus.stream(Streams.EXEC_FILLS)]


def updates(bus: SyncTestBus) -> list[OrderUpdate]:
    return [e.decode() for e in bus.stream(Streams.EXEC_ORDER_UPDATES)]


# ---------------------------------------------------------------- ack


def test_order_is_acked_and_pending(env) -> None:
    bus, clock, broker = env
    publish_order(bus, clock, make_order("ord-1"))

    ups = bus.stream(Streams.EXEC_ORDER_UPDATES)
    assert len(ups) == 1
    update = ups[0].decode()
    assert update.order_id == "ord-1"
    assert update.status is OrderStatus.ACKED
    assert ups[0].ts_event == clock.now_ns()

    assert set(broker.pending) == {"ord-1"}
    assert isinstance(broker.pending["ord-1"], Order)
    assert fills(bus) == []


# ----------------------------------------------------- anti-lookahead


def test_signal_bar_never_fills_its_own_order(env) -> None:
    """The single most important property: an order generated by a bar
    close cannot fill on that bar -- not even when the bar is replayed
    -- and fills on the next bar's open instead."""
    bus, clock, broker = env
    signal_bar = make_bar(T0, o=100.0)
    publish_bar(bus, signal_bar)  # strategy decides on this close
    assert fills(bus) == []

    clock.advance_to(signal_bar.ts_close)  # the order exists only now
    publish_order(bus, clock, make_order("ord-al"))
    assert "ord-al" in broker.pending

    publish_bar(bus, signal_bar)  # replay of the signal bar
    assert fills(bus) == []
    assert "ord-al" in broker.pending

    next_bar = make_bar(T0 + BAR_NS, o=102.0, h=103.0, lo=101.0, c=102.5)
    publish_bar(bus, next_bar)  # ts_open == placed_ts -> eligible
    got = fills(bus)
    assert len(got) == 1
    assert got[0].price == 102.0 * (1 + SLIPPAGE_BPS / 1e4)
    assert got[0].ts_fill == next_bar.ts_open
    assert broker.pending == {}


def test_bar_older_than_order_never_fills(env) -> None:
    bus, clock, broker = env
    clock.advance_to(T0 + 5 * BAR_NS)
    publish_order(bus, clock, make_order("ord-old"))

    publish_bar(bus, make_bar(T0 + 4 * BAR_NS))  # strictly before placed_ts
    assert fills(bus) == []
    assert "ord-old" in broker.pending


