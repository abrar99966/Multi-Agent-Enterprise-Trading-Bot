def test_multiple_pending_orders_fill_on_one_bar(env) -> None:
    bus, clock, broker = env
    publish_order(bus, clock, make_order("ord-1", side=Side.BUY))
    publish_order(bus, clock, make_order("ord-2", side=Side.SELL, qty=5.0))
    publish_bar(bus, make_bar(T0, o=100.0))

    got = fills(bus)
    assert [f.order_id for f in got] == ["ord-1", "ord-2"]
    assert got[0].price == 100.0 * (1 + SLIPPAGE_BPS / 1e4)
    assert got[1].price == 100.0 * (1 - SLIPPAGE_BPS / 1e4)
    assert broker.pending == {}
