# ---------------------------------------------------- fees and fills


def test_fees_are_qty_times_price_times_fee_bps(env) -> None:
    bus, clock, broker = env
    publish_order(bus, clock, make_order("ord-f", qty=10.0))
    publish_bar(bus, make_bar(T0, o=100.0))

    got = fills(bus)
    assert len(got) == 1
    assert got[0].fees == 10.0 * got[0].price * FEE_BPS / 1e4
    assert got[0].fees == pytest.approx(0.10002)  # 10 * 100.02 * 1e-4


def test_fill_event_fields_and_filled_update(env) -> None:
    bus, clock, broker = env
    publish_order(bus, clock, make_order("ord-x"))
    bar = make_bar(T0, o=100.0)
    publish_bar(bus, bar)

    got = fills(bus)
    assert len(got) == 1
    fill = got[0]
    assert fill.fill_id == "fill-ord-x-1"
    assert fill.order_id == "ord-x"
    assert fill.intent_id == "int-ord-x"
    assert fill.strategy_id == "strat-1"
    assert fill.symbol == "RELIANCE"
    assert fill.ts_fill == bar.ts_open
    assert fill.qty == 10.0  # full quantity, single fill

    assert [u.status for u in updates(bus)] == [
        OrderStatus.ACKED,
        OrderStatus.FILLED,
    ]
    # Fill is published before the FILLED update.
    fill_idx = next(i for i, e in enumerate(bus.events) if e.type == "Fill")
    filled_idx = next(
        i
        for i, e in enumerate(bus.events)
        if e.type == "OrderUpdate"
        and e.payload["status"] == OrderStatus.FILLED.value
    )
    assert fill_idx < filled_idx
    assert broker.pending == {}

    # Filled orders never fill again on later bars.
    publish_bar(bus, make_bar(T0 + BAR_NS, o=105.0, h=106.0, lo=104.0))
    assert len(fills(bus)) == 1


